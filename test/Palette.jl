using Omics

using Test: @test

# ----------------------------------------------------------------------------------------------- #

# ---- #

for (nu_, re) in (
    ([1.0], Omics.Palette.bwr),
    ([1], Omics.Palette.MO),
    ([1, 2], Omics.Palette.BI),
    ([1, 2, 3], Omics.Palette.CA),
)

    @test Omics.Palette.pick(nu_) === re

end

# ---- #

const RG_ = Omics.Palette.make(["#ff0000", "#00ff00", "#0000ff"])

for (nu, re) in (
    # Indexed.
    (1, "#ff0000"),
    (2, "#00ff00"),
    (3, "#0000ff"),
    # Normalized between 0 and 1.
    (-Inf, "#ff0000"),
    (-0.1, "#ff0000"),
    (0.0, "#ff0000"),
    (0.01, "#fa0500"),
    (0.5, "#00ff00"),
    (0.99, "#0005fa"),
    (1.0, "#0000ff"),
    (1.1, "#0000ff"),
    (Inf, "#0000ff"),
    # Normalized between the extrema.
    ([NaN], ["#00ff00"]),
    ([-1], ["#00ff00"]),
    ([0], ["#00ff00"]),
    ([4], ["#00ff00"]),
    (1:3, ["#ff0000", "#00ff00", "#0000ff"]),
    (1:4, ["#ff0000", "#55aa00", "#00aa55", "#0000ff"]),
)

    @test Omics.Palette.color(nu, RG_) == re

end

