        [
            (0.0, "#000001"),
            (0.25, "#000002"),
            (0.5, "#000003"),
            (0.75, "#000004"),
            (1.0, "#000005"),
        ],
    ),
    (
        ["#000001", "#000002", "#000003", "#000004", "#000005", "#000006"],
